      </p>

      <p class="py-6">Luckily, there are tools that can fill the support gap brought about by the ‘new normal’ and improve employee productivity and efficiency. Knowledge bases (FAQs), self-service portals, and chatbots empower employees to self-serve when and where they need support. </p>

