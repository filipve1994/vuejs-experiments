        <li class="bullet-list-li article-li">Smoother onboarding for remote employees</li>
        <li class="bullet-list-li article-li">Streamlining internal communications</li>
        <li class="bullet-list-li article-li">Standardizing procedures and minimizing the risk of error</li>
        <li class="bullet-list-li article-li">Reducing the load on the HR, IT, and Facility departments</li>
        <li class="bullet-list-li article-li">Lessening the burden on employers to staff human-assisted channels</li>
      </ul>

      <p class="py-6">The flexibility of employee self-service lends itself to nearly any department within an organization. Here are a few use cases that are especially relevant for companies who have transitioned to remote or hybrid work environments:</p>


      <ul class="bullet-list article-ul">
        <li class="bullet-list-li article-li">
          <b class="text-green-500 no-underline hover:underline">IT Support</b> – repetitive help desk requests, login information and password resets, device/machine support – Employees transitioning to working from home will inevitably have questions, especially if they are using new technology set-ups that they aren’t familiar with. It is critical that employees have access to the technical support they need to do their jobs. Using a chatbot that automatically responds to requests gives them 24/7 access to self-service. Regardless of when or where they run into problems, they can find information and access troubleshooting information to help resolve their issues without relying on face-to-face interaction.
        </li>

        <li class="bullet-list-li article-li">
          <b class="text-green-500 no-underline hover:underline">HR Support</b> – company policies/procedures information, vacation requests, payroll and benefits questions, staff training, expense report help – As a result of the coronavirus pandemic, many companies have put established new policies and procedures for employees or made updates to existing ones. With so much new information – combined with new personal and working routines – employees require an easy way to find the answers they need quickly. Self-service tools remove the need for employees to search through an entire policy document for the answer to one specific question by instantly pulling out the piece of information that is relevant to them. Some technology can also be set up with an anonymity feature to give employees peace of mind when submitting HR-related requests that are more personal in nature.

        </li>

        <li class="bullet-list-li article-li">
          <b class="text-green-500 no-underline hover:underline">Facility Support</b> - maintenance requests, building information, supplies/re-order assistance - Even if your company has made the shift to remote work, employees may still need to visit a physical location from time to time. However, due to capacity restrictions and reduced occupancy, keeping your office fully staffed doesn’t make sense and opens your building services team up to unnecessary risks. With the self-service portal, employees can easily find up-to-date information regarding new office procedures, place requests, and track the status of their open work orders.

        </li>


      </ul>

      <p class="py-6">
        Though implementing a self-service support system for employees will pay off in terms of unified communication and consistency across your organization, they must be carefully selected and managed to reach their full potential.
      </p>

      <p class="py-6">
        Common barriers to self-service adoption include:
      </p>

      <ol class="bullet-list article-ul">

        <li class="bullet-list-li article-li">Failure to design self-service capabilities suited for real-world use by not factoring in employees’ wants and needs.
        </li>

        <li class="bullet-list-li article-li">Smoother onboarding for remote employees</li>

        <li class="bullet-list-li article-li">Insufficient explanation of how and why everyone will benefit from using it.
        </li>

        <li class="bullet-list-li article-li">Neglecting to update the system with new, up-to-date information</li>
      </ol>


